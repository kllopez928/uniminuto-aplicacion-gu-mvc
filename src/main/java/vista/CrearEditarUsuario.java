    private String guardarCliente() {
        if (txtIdentificacion.getText().equals("") || txtPrimerNombre.getText().equals("")
                || txtSegundoNombre.getText().equals("") || txtPrimerApellido.getText().equals("")
                || txtSegundoApellido.getText().equals("") || txtTelefono.getText().equals("")
                || txtCorreoElectronico.getText().equals("")) {
            return "Verifique todos los campos sean diligenciados correctamente.";
        }

        if (!StringUtils.validarNumeroNoContengaLetras(txtIdentificacion.getText())) {
            return "Verifique que el campo identificacion solo tenga numeros y que el campo no este vacio";
        }

        if (!StringUtils.validarEmail(txtCorreoElectronico.getText())) {
            return "Ingrese un correo valido y que el campo no este vacio";
        }

        int opcion = JOptionPane.showConfirmDialog(null, "Estas seguro de AÃ±adir  el cliente con identificacion : " + txtIdentificacion.getText());
        // Si se ha confirmado la creacion del cliente se va guardar en bd
        if (opcion == 0) {
            ClienteDTO cliente = new ClienteDTO();
            cliente.setIdentificacion(Long.parseLong(txtIdentificacion.getText()));
            cliente.setPrimerNombre(txtPrimerNombre.getText());
            cliente.setSegundoNombre(txtSegundoNombre.getText());
            cliente.setPrimerApellido(txtPrimerApellido.getText());
            cliente.setSegundoApellido(txtSegundoApellido.getText());
            cliente.setTelefono(txtTelefono.getText());
            cliente.setCorreoElectronico(txtCorreoElectronico.getText());
            CargoDTO cargo = (CargoDTO) comboBoxCargo.getSelectedItem();
            cliente.setCargo(cargo);
            SupermercadoDTO supermercado = (SupermercadoDTO) comboBoxSupermercado.getSelectedItem();
            cliente.setSupermercado(supermercado);
            return controller.guardarCliente(cliente);
        }
        return null;
    }

