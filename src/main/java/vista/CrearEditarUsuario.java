    private void cargarCliente(ClienteDTO cliente) {
        Long identificacion = cliente.getIdentificacion();
        String primerNombre = cliente.getPrimerNombre();
        String segundoNombre = cliente.getSegundoNombre();
        String primerApellido = cliente.getPrimerApellido();
        String segundoApellido = cliente.getSegundoApellido();
        String telefono = cliente.getTelefono();
        String correoElectronico = cliente.getCorreoElectronico();

        txtIdentificacion.setText(String.valueOf(identificacion));
        txtPrimerNombre.setText(primerNombre);
        txtSegundoNombre.setText(segundoNombre);
        txtPrimerApellido.setText(primerApellido);
        txtSegundoApellido.setText(segundoApellido);
        txtTelefono.setText(telefono);
        txtCorreoElectronico.setText(correoElectronico);

        // mediante el uso de streams obtendremos el cargo asociado al cliente que se quiere editar...
        List<CargoDTO> listaCargos = controller.obtenerCargos();
        int indiceCargo = IntStream.range(0, listaCargos.size())
                .filter(cli -> listaCargos.get(cli).getId().equals(cliente.getCargo().getId()))
                .findFirst().orElse(-1);
        comboBoxCargo.setSelectedIndex(indiceCargo);

        // mediante el uso de streams obtendremos el supermercado asociado al cliente que se quiere editar...
        List<SupermercadoDTO> listaSupermercados = controller.obtenerSupermercados();
        int indiceSupermercado = IntStream.range(0, listaSupermercados.size())
                .filter(cli -> listaSupermercados.get(cli).getId().equals(cliente.getSupermercado().getId()))
                .findFirst().orElse(-1);
        comboBoxSupermercado.setSelectedIndex(indiceSupermercado);

    }

    private void cargarModeloCargos() {
        List<CargoDTO> listaCargos = controller.obtenerCargos();
        for (CargoDTO cargo : listaCargos) {
            modeloCargo.addElement(cargo);
        }
    }

    private void cargarModeloSupermercados() {
        List<SupermercadoDTO> listaSupermercados = controller.obtenerSupermercados();
        for (SupermercadoDTO supermercado : listaSupermercados) {
            modeloSupermecado.addElement(supermercado);
        }
    }

